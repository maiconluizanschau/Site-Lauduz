<h3>Seja voluntário da saúde</h3>
</div>
<p>A Lauduz disponibiliza sem custos a sua plataforma para Instituições, Hospitais e Prefeituras que desejam prestar teleatendimentos gratuitos, rápidos e de qualidade para os pacientes com sintomas do novo coronavírus. 
A Organização fornece ainda todas as informações necessárias e auxílio possíveis para a implantação nas cidades, com todo o know-how de quem foi pioneira em telemedicina no Brasil e que busca proteger o médico, o paciente, os estudantes de medicina e os demais profissionais da saúde, prezando sempre pela Ética e Profissionalismo.<br/>As Universidades com curso de Medicina de Santa Maria, UFN e UFSM, já se pronunciaram que vão validar as horas de seus internos, os quais estão trabalhando diariamente com escalas de trabalho em um sistema de preceptoria permitido dentro da Plataforma da Lauduz.<br/>Então se você é interno do curso de Medicina (9o ao 12o semestre) e tem interesse em ajudar a consultar pacientes em sua cidade ou até mesmo em outras cidades do Brasil e ainda tentar validar suas horas para o internato, responda esse questionário que iremos te explicar como funciona para você implantar em seu Hospital Escola/Instituição. Formulário para voluntários internos do curso de medicina.

<br/>
Se você é médico e deseja ser voluntário Lauduz fornecendo teleatendimentos gratuitos e de qualidade, com respaldo legal e com toda a orientação baseada nos princípios do Código de Ética Médica, preencha esse formulário! Você é muito importante nessa luta! Formulário para médicos voluntários.

	<br/>
Se você é enfermeiro, psicólogo ou de outra área da saúde, também há espaço para você ajudar a combater a covid-19 junto com a gente. Pode ser importante levar esse tipo de auxílio para a sua cidade. Em Itapema/SC, por exemplo, os enfermeiros auxiliam os médicos no atendimento primário e agora estão fazendo isso online junto com a Lauduz. Formulário para voluntários da área da saúde 
	<br/>
Aos estudantes de medicina do 1o ao 8o ano, vocês podem ser embaixadores Lauduz, levando as teleconsultas para a sua cidade, podendo orientar os médicos de como funciona todo o sistema. Já tem estudantes de medicina fazendo a diferença nessa luta contra o novo coronavírus. Contamos com vocês também! Formulário para estudantes de medicina.
	</p>
	<h3>Dado importante:</h3>
	<p>O Primeiro Hospital Afiliado da Faculdade de Medicina da Universidade de Zhejiang na China está seguindo a mesmo caminho da Lauduz e já atendeu mais de 10.000 pacientes e salientou a importância desse atendimento na diminuição da transmissão e infecção cruzada. A Lauduz já atendeu mais de 2.000 pacientes e aproximadamente 99% dos pacientes consultados em nossa plataforma são mantidos em casa e iriam desnecessariamente até um pronto-atendimento ou mesmo um hospital.<br/>
	Juntos podemos atender milhões de pacientes no Brasil.</p>
